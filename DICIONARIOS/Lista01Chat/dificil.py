# Exercício Difícil: Atualização de Estoque de Produtos

# Enunciado:
# Crie um dicionário que armazene o estoque de uma loja, onde as chaves são os nomes dos produtos e os valores são a quantidade em estoque.
# Implemente funções para adicionar um produto, remover um produto e atualizar a quantidade de um produto existente. 
# No final, mostre o estoque atualizado.